#!/bin/bash
###################################################################
# MPI Installation Script
# Version : 3.2
###################################################################


###################################################################
# Read environtment variables
###################################################################
source /opt/install-env.sh


###################################################################
# Extract MPICH-3.2 to /usr/local/
###################################################################
tar xzf $MPICH_INS -C $MPICH_INSTALLATION_DIR


###################################################################
# Compile and install MPICH
###################################################################
cd $MPICH_DIR
./configure
make
make install
